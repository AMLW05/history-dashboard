// Function to render resources (called by app.js)
function renderResources() {
    const container = document.getElementById('resources-container');
    if (!container) return;

    let html = '<div class="section-title">üìö Course Resources</div>';
    html += '<p class="instruction-text">Everything you need for success in HUM-130</p>';

    Object.values(courseResources).forEach(resource => {
        html += `<div class="resource-section">
            <h2 class="resource-title">${resource.icon} ${resource.title}</h2>`;

        resource.sections.forEach(section => {
            html += `<div class="resource-block">
                <h3 class="resource-heading">${section.heading}</h3>
                <div class="resource-content">${section.content.replace(/\n/g, '<br>')}</div>
            </div>`;
        });

        html += `</div>`;
    });

    container.innerHTML = html;
}

